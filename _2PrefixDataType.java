package com.dhanush.datatypes;

public class _2PrefixDataType {
	public static void main(String[] args) {
		int a=010; //octal
		int b=0X12;//Hexa
		int c=0010; //octal
		int d=0b001; //binary
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
	}
}
